const mongoose = require('mongoose');

const registerSchema = new mongoose.Schema({

    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    },
    confirm_password: {
        type: String,
        required: true
    }

})
const register = mongoose.model('register',registerSchema);
module.exports = register;